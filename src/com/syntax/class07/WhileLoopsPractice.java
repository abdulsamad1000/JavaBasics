package com.syntax.class07;

public class WhileLoopsPractice {

	public static void main(String[] args) {
	int time=10;
	if (time<12) {
		System.out.println("Morning");
		System.out.println("=====================");
		// this is loop
		while (time<12) {
			System.out.println("Good Morning");
			time++;
		}
		System.out.println("Hello");
	}

	}

}
