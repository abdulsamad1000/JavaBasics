package com.syntax.class07;

public class IncrementAndDecrementPractice {

	public static void main(String[] args) {
		// here we increment which mean we add one number to it.
		// we have 10 and we add one on it which make it 11
		int num=10;
		num++;
		System.out.println(num);
		
		System.out.println();
		System.out.println("================");
		// here we decrement which mean we mines one number.
		// we 20 and we mines one from it which make it 19
		int num1=20;
		num1--;
		System.out.println(num1);
		
		

	}

}
